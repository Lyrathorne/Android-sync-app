    fun items(
        category: EmojiCategory,
        recent: List<String>,
        favorites: List<String> = emptyList(),
    ): List<String> = when (category) {
        EmojiCategory.RECENT -> recent
        EmojiCategory.FAVORITES -> favorites
        EmojiCategory.SEARCH -> emptyList()
        else -> items[category].orEmpty()
    }

    fun searchOptions(): List<String> = searchGroups.keys.toList()

    fun search(query: String): List<String> {
        val normalized = query.trim().lowercase()
        if (normalized.isBlank()) return emptyList()
        return searchGroups.entries
            .filter { (label, group) ->
                label.contains(normalized) || group.aliases.any { it.contains(normalized) || normalized.contains(it) }
            }
            .flatMap { it.value.items }
            .distinct()
            .take(160)
    }

    fun totalCount(): Int = items.values.sumOf(List<String>::size)

    private data class SearchGroup(val aliases: List<String>, val items: List<String>)

    private val searchGroups: LinkedHashMap<String, SearchGroup> by lazy(LazyThreadSafetyMode.PUBLICATION) {
        linkedMapOf(
            "Смайлы" to SearchGroup(listOf("улыбка", "эмоции", "лицо", "smile", "face"), items[EmojiCategory.SMILEYS].orEmpty()),
            "Любовь" to SearchGroup(listOf("сердце", "романтика", "heart", "love"), items[EmojiCategory.SYMBOLS].orEmpty().take(24)),
            "Люди" to SearchGroup(listOf("человек", "профессия", "people", "person"), items[EmojiCategory.PEOPLE].orEmpty()),
            "Жесты" to SearchGroup(listOf("рука", "палец", "hand", "gesture"), items[EmojiCategory.GESTURES].orEmpty()),
            "Животные" to SearchGroup(listOf("кот", "собака", "природа", "animal", "cat", "dog"), items[EmojiCategory.ANIMALS].orEmpty()),
            "Еда" to SearchGroup(listOf("напитки", "фрукты", "food", "drink"), items[EmojiCategory.FOOD].orEmpty()),
            "Спорт" to SearchGroup(listOf("игра", "музыка", "sport", "game"), items[EmojiCategory.ACTIVITIES].orEmpty()),
            "Транспорт" to SearchGroup(listOf("машина", "самолёт", "путешествие", "car", "travel"), items[EmojiCategory.TRAVEL].orEmpty()),
            "Предметы" to SearchGroup(listOf("вещи", "техника", "objects", "phone"), items[EmojiCategory.OBJECTS].orEmpty()),
